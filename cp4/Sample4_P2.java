class Sample4_P2 
{
    public static void main(String args[])
    {
        int num1 = 0-4;
        double num2 = 3.14*2;
        double num3 = (double)5/3;
        int num4 = 30%7;
        double num5 = (7+32)/(double)5; 
        System.out.println(0-4);

        System.out.println("0-4는 "+num1+"입니다.");
        System.out.println("3.14×2는 "+num2+"입니다.");
        System.out.println("5/3은 "+num3+"입니다.");
        System.out.println("30÷7의 나머지값은 "+num4+"입니다.");
        System.out.println("(7+32)÷5는 "+num5+"입니다.");
    }
}
